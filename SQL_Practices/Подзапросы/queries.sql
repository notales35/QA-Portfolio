-- Задача 1: Поиск адресов и почтовых индексов в конкретном городе (London)
-- Демонстрирует умение связывать таблицы без использования JOIN, через оператор IN
SELECT address, postal_code 
FROM address 
WHERE city_id IN (
    SELECT city_id 
    FROM city 
    WHERE city = 'London'
);

